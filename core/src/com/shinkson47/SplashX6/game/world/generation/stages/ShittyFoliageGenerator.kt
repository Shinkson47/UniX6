package com.shinkson47.SplashX6.game.world.generation.stages

import com.badlogic.gdx.math.MathUtils
import com.shinkson47.SplashX6.game.world.Tile
import com.shinkson47.SplashX6.game.world.WorldTerrain
import com.shinkson47.SplashX6.game.world.generation.ModifyingGenerationStage

/**
 * # TODO
 * @author [Jordan T. Gray](https://www.shinkson47.in) on 20/07/2021
 * @since v1
 * @version 1
 */
class ShittyFoliageGenerator : ModifyingGenerationStage() {
    override fun execute(it: WorldTerrain) {
        var x: Int
        var y: Int
        var s: String

        with(it) {
            for (i in 0..WorldTerrain.FOLIAGE_QUANTITY_MAX) {
                x = MathUtils.random(0, height - 1)
                y = MathUtils.random(0, width - 1)

                if (getTile(x,y,heightTiles) != null) continue // Skip if there's a hill there.

                s = getFoliageTile(worldTiles[y][x]!!.tileName)
                if (s != "") FoliageLayerTiles[y][x] = Tile(s + "1")
            }
        }
    }

    private fun getFoliageTile(s: String): String {
        return when (s) {
            "d_d_d_d" -> "cactus0"
            "g_g_g_g" -> "grasses0"
            "p_p_p_p" -> "bigtree0"
            "t_t_t_t" -> "bush0"
            "a_a_a_a" -> ""
            else -> ""
        }
    }
}