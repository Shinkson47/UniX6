package com.shinkson47.SplashX6.utility.configuration

import com.badlogic.gdx.scenes.scene2d.ui.TooltipManager
import com.shinkson47.SplashX6.rendering.windows.NodeInfo
import com.shinkson47.SplashX6.utility.Assets
import com.shinkson47.SplashX6.utility.Assets.REF_LANG
import com.shinkson47.SplashX6.utility.Utility
import java.util.MissingResourceException

object LanguageConfig {
        enum class Languages {
            en, fr, de;
        }

        @JvmStatic
        fun local(key: String): String {
            return if (key.startsWith("!")) key.substring(1) else try {
                REF_LANG[key]
            } catch (e: MissingResourceException) {
                e.message?.let { Utility.warnDev(it) }
                "???$key???"
            }
        }

        val ttManager = TooltipManager()
        @NodeInfo("Time of hovering over an object before a tooltip is displayed.")
        var ttInitialTime: Float = Assets.REF_PREFERENCES.getFloat("prefs.tooltip.initialtime", 0.5F)
            set(value) {
                ttManager.initialTime = value
                Assets.REF_PREFERENCES.putFloat("prefs.tooltip.initialtime", value)
                Assets.REF_PREFERENCES.flush()
                field = value
            }

        @JvmStatic
        @NodeInfo("After displaying a tooltip, and before reset, time take to display tooltips.")
        var ttSubsequentTime: Float = Assets.REF_PREFERENCES.getFloat("prefs.tooltip.subsequenttime", ttManager.subsequentTime)
            set(value) {
                ttManager.subsequentTime = value
                Assets.REF_PREFERENCES.putFloat("prefs.tooltip.subsequenttime", value)
                Assets.REF_PREFERENCES.flush()
                field = value
            }

        @JvmStatic
        @NodeInfo("Time with no tooltips to reset to initial delay.")
        var ttResetTime: Float = Assets.REF_PREFERENCES.getFloat("prefs.tooltip.resettime", ttManager.resetTime)
            set(value) {
                ttManager.resetTime = value
                Assets.REF_PREFERENCES.putFloat("prefs.tooltip.resettime", value)
                Assets.REF_PREFERENCES.flush()
                field = value
            }
}

