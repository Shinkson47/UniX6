class W_AdvancementProduction : StageWindow("!Tech advancements") {

    val tab = TechProductionTab()

    init {
        expandfill(add(tab))
        row()
        pack()

        tab.btnAddButton.touchable = Touchable.disabled
        tab.btnRemoveButton.touchable = Touchable.disabled

    }

    override fun show() {
        refresh()
    }

    public override fun refresh() {
        tab.refresh(GameData.player!!.advancementProuction)
    }

}