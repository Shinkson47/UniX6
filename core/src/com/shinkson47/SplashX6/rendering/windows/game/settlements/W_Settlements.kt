package com.shinkson47.SplashX6.rendering.windows.game.settlements

import com.badlogic.gdx.scenes.scene2d.ui.SelectBox
import com.shinkson47.SplashX6.game.GameData
import com.shinkson47.SplashX6.game.Hypervisor
import com.shinkson47.SplashX6.game.cities.Settlement
import com.shinkson47.SplashX6.rendering.ui.StageWindow
import com.shinkson47.SplashX6.utility.Assets
import com.shinkson47.SplashX6.utility.Utility

/**
 * # A window that displays and manages the player's settlements and the production within
 * @author [Jordan T. Gray](https://www.shinkson47.in) on 21/06/2021
 * @since v1
 * @version 0.0.3
 */
class W_Settlements : StageWindow("generic.game.settlements") {

    private val settlements: SelectBox<Settlement> = SelectBox(Assets.REF_SKIN_W95)
    val productionTab = UnitTab()
    val improvementsTab = ImprovementsTab()
    val statsTab = StatsTab()

    init {
        settlements.addListener(LambdaChangeListener {
            settlements.selected?.let { Hypervisor.camera_focusOn(it) }
            refresh()
        })

        add(settlements)
            .fillX()
            .expandX()
            .row()

        val contentCell = add()

        tabs(
            contentCell,

            listOf(
                productionTab,
                improvementsTab,
                statsTab
            ),

            listOf(
                "!Production",
                "!Improvements",
                "!Stats"
            )
        )

        Hypervisor.turn_unhook(this)
        Hypervisor.turn_hook { Hypervisor.turn_asyncTask(this) }
    }

    override fun show() {
        refresh()

        invalidate()
        pack()
        height = 600f
    }

    public override fun refresh() {
        settlements.items = Utility.CollectionToGDXArray(GameData.localPlayer!!.settlements)
        if (settlements.selected == null) return
        productionTab.refresh(settlements.selected.unitProduction)
        improvementsTab.refresh(settlements.selected.improvementProduction)
        statsTab.refresh(settlements.selected)
    }
}