package com.shinkson47.SplashX6.rendering.ui

import com.badlogic.gdx.ScreenAdapter
import com.badlogic.gdx.scenes.scene2d.Stage
import com.badlogic.gdx.utils.viewport.ScalingViewport
import com.shinkson47.SplashX6.utility.debug.Console
import com.shinkson47.SplashX6.utility.configuration.GraphicalConfig
import java.util.function.Consumer

/**
 * A screen adapter that contains a stage for scaling UI according
 * to [GraphicalConfig.scalingMode].
 *
 * Since this is used as a super class to all X6 screens,
 * it may also contain some extra commons, such as the console.
 * @author [Jordan T. Gray](https://www.shinkson47.in) on 14/06/2021
 * @since v1
 * @version 1.1
 */
abstract class ScalingScreenAdapter() : ScreenAdapter() {

    @JvmField var width =  GraphicalConfig.displayMode.width.toFloat() .coerceAtLeast(GraphicalConfig.MIN_STAGE_WITDH.toFloat())
    @JvmField var height = GraphicalConfig.displayMode.height.toFloat().coerceAtLeast(GraphicalConfig.MIN_STAGE_HEIGHT.toFloat())

    private val view = ScalingViewport(GraphicalConfig.scalingMode, width, height)
    @JvmField val stage = Stage(view)

    fun dialog(titleKey: String, textKey: String, handler: Consumer<Boolean>?) =
        StageWindow.dialog(stage, titleKey, textKey, "", "", handler)

    init {
        //stage.isDebugAll = Client.DEBUG_MODE
    }

    abstract fun doResize(width: Int, height: Int)
    override fun resize(width: Int, height: Int) {
        view.scaling = GraphicalConfig.scalingMode

        view.setWorldSize(this.width, this.height)
        stage.viewport.update(width, height)

        doResize(this.width.toInt(), this.height.toInt())
    }

    override fun show() {
        Console.resetInputProcessing()
    }

    // TODO the stage isn't rendered here????
    override fun render(delta: Float) {
        try {
            Console.draw()
        } catch (e : Exception) {
            Utility.warnDev("Console failed to render.")

        }
    }
}